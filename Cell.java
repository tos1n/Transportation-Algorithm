
public class Cell {

    private int price;
    private int currentSupply = 0;

    public int getPrice() {
	return price;
    }

    public void setPrice(int price) {
	this.price = price;
    }

    public int getCurrentSupply() {
	return currentSupply;
    }

    public void setCurrentSupply(int currentSupply) {
	this.currentSupply = currentSupply;
    }

}
